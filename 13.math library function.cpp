#include <iostream>
#include <cmath>

using namespace std;

int main() {
    double number = 4.0;

    cout << "Square root of " << number << " is: " << sqrt(number) << endl;
    cout << "Absolute value of " << number << " is: " << fabs(number) << endl;
    cout << "Ceiling of " << number << " is: " << ceil(number) << endl;

    return 0;
}
